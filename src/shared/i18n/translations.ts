export const TRANSLATIONS = { es, en, pt, de } as const;

export type TranslationKey = keyof typeof es;

export type TranslationParams = Record<string, string | number>;

export const LANGUAGE_OPTIONS: readonly LanguageDetails[] = [
    LANGUAGE_DETAILS.es,
    LANGUAGE_DETAILS.en,
    LANGUAGE_DETAILS.pt,
    LANGUAGE_DETAILS.de,
] as const;

export function isLanguage(value: string): value is Language {
    return SUPPORTED_LANGUAGES.includes(value as Language);
}

function interpolate(template: string, params?: TranslationParams): string {
    if (!params) return template;
    return template.replace(/\{\{(\w+)\}\}/g, (_, token: string) => String(params[token] ?? ""));
}

export function getTranslation(language: Language, key: TranslationKey, params?: TranslationParams): string {
    const template = TRANSLATIONS[language][key] ?? TRANSLATIONS.es[key];
    return interpolate(template, params);
}
